static void set_defaults(mm_config_t *c) {
	c->c_dc_uf = 0.0f;            // 0 -> keep the compiled bench (2 mF) gains
	c->autoarm_en = false;        // armed-only posture by default
	c->autoarm_v_clamp = 0.0f;
	c->autoarm_i_floor = 0.0f;
	c->autoarm_i_max = 0.0f;
	c->autoarm_clamp_en = true;
	c->autoarm_floor_en = true;
	c->autoarm_allow_start = false;
}

static void store_eeprom(void) {
	eeprom_var v;
	v.as_float = m_config.c_dc_uf;
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_C_DC);
	v.as_u32 = (m_config.autoarm_en ? MM_FLAG_AUTOARM : 0) |
			(m_config.autoarm_clamp_en ? MM_FLAG_CLAMP_EN : 0) |
			(m_config.autoarm_floor_en ? MM_FLAG_FLOOR_EN : 0) |
			(m_config.autoarm_allow_start ? MM_FLAG_ALLOW_START : 0);
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_FLAGS);
	v.as_float = m_config.autoarm_v_clamp;
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_V_CLAMP);
	v.as_float = m_config.autoarm_i_floor;
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_I_FLOOR);
	v.as_float = m_config.autoarm_i_max;
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_I_MAX);
	// Write the validity marker last so a partial write is never seen as valid.
	v.as_u32 = MM_EE_MAGIC;
	conf_general_store_eeprom_var_custom(&v, MM_EE_ADDR_MAGIC);
}

static void load_eeprom(void) {
	eeprom_var v;
	if (!conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_MAGIC) ||
			v.as_u32 != MM_EE_MAGIC) {
		set_defaults(&m_config);
		return;
	}
	conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_C_DC);
	m_config.c_dc_uf = v.as_float;
	conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_FLAGS);
	uint32_t flags = v.as_u32;
	m_config.autoarm_en = (flags & MM_FLAG_AUTOARM) != 0;
	m_config.autoarm_clamp_en = (flags & MM_FLAG_CLAMP_EN) != 0;
	m_config.autoarm_floor_en = (flags & MM_FLAG_FLOOR_EN) != 0;
	m_config.autoarm_allow_start = (flags & MM_FLAG_ALLOW_START) != 0;
	conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_V_CLAMP);
	m_config.autoarm_v_clamp = v.as_float;
	conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_I_FLOOR);
	m_config.autoarm_i_floor = v.as_float;
	conf_general_read_eeprom_var_custom(&v, MM_EE_ADDR_I_MAX);
	m_config.autoarm_i_max = v.as_float;
}

// Derive the clamp voltage-loop gains from C_dc and push them to the motor(s).
// C_dc <= 0 leaves the compiled bench defaults (set at mcpwm_foc_init) in place.
static void push_gains(void) {
	if (m_config.c_dc_uf < 1.0f) {
		return;
	}
	const float wn = 2.0f * (float)M_PI * MM_BC_WN_HZ;   // rad/s
	const float c = m_config.c_dc_uf * 1e-6f;            // F
	const float ki = wn * wn * c;
	const float kp = 2.0f * MM_BC_ZETA * wn * c;
	mcpwm_foc_set_bus_clamp_gains(kp, ki);
}

static void autoarm(void) {
	if (!m_config.autoarm_en) {
		return;
	}
	uint8_t flags = (m_config.autoarm_clamp_en ? 0x01 : 0) |
			(m_config.autoarm_floor_en ? 0x02 : 0) |
			(m_config.autoarm_allow_start ? 0x04 : 0);
	if (flags == 0) {
		return;   // nothing to enable
	}
	// Guards for the unattended (boot) arm. A boot-armed clamp must carry an
	// explicit current ceiling — it must never silently own the full motor
	// current limit — and, if the voltage clamp is enabled, a real setpoint
	// (0 would truncate to l_min_vin+2 and burn continuously the moment the
	// motor spins). Refuse loudly rather than arm a nonsensical config.
	if (m_config.autoarm_i_max < 0.01f) {
		commands_printf("mm_config: auto-arm skipped — set a nonzero i_max first");
		return;
	}
	if (m_config.autoarm_clamp_en && m_config.autoarm_v_clamp < 1.0f) {
		commands_printf("mm_config: auto-arm skipped — set a nonzero v_clamp first");
		return;
	}
	bool ok = mc_interface_conf_bus_clamp(m_config.autoarm_v_clamp,
			m_config.autoarm_i_floor, m_config.autoarm_i_max, flags);
	commands_printf(ok
			? "mm_config: bus clamp auto-armed (v_clamp %.1f, i_floor %.2f, i_max %.1f)"
			: "mm_config: bus clamp auto-arm REJECTED (HFI sensor mode or other motor owns the bus)",
			(double)m_config.autoarm_v_clamp, (double)m_config.autoarm_i_floor,
			(double)m_config.autoarm_i_max);
}

static int get_cfg(uint8_t *buffer, bool is_default) {
	mm_config_t c = m_config;
	if (is_default) {
		set_defaults(&c);
	}
	int32_t ind = 0;
	buffer_append_uint32(buffer, MM_CONFIG_SIGNATURE, &ind);
	buffer_append_float32_auto(buffer, c.c_dc_uf, &ind);
	buffer[ind++] = c.autoarm_en ? 1 : 0;
	buffer_append_float32_auto(buffer, c.autoarm_v_clamp, &ind);
	buffer_append_float32_auto(buffer, c.autoarm_i_floor, &ind);
	buffer_append_float32_auto(buffer, c.autoarm_i_max, &ind);
	buffer[ind++] = c.autoarm_clamp_en ? 1 : 0;
	buffer[ind++] = c.autoarm_floor_en ? 1 : 0;
	buffer[ind++] = c.autoarm_allow_start ? 1 : 0;
	return ind;
}

static bool set_cfg(uint8_t *buffer) {
	int32_t ind = 0;
	uint32_t sig = buffer_get_uint32(buffer, &ind);
	if (sig != MM_CONFIG_SIGNATURE) {
		return false;
	}
	mm_config_t c;
	c.c_dc_uf = buffer_get_float32_auto(buffer, &ind);
	c.autoarm_en = buffer[ind++] != 0;
	c.autoarm_v_clamp = buffer_get_float32_auto(buffer, &ind);
	c.autoarm_i_floor = buffer_get_float32_auto(buffer, &ind);
	c.autoarm_i_max = buffer_get_float32_auto(buffer, &ind);
	c.autoarm_clamp_en = buffer[ind++] != 0;
	c.autoarm_floor_en = buffer[ind++] != 0;
	c.autoarm_allow_start = buffer[ind++] != 0;

	utils_truncate_number(&c.c_dc_uf, 0.0f, MM_C_DC_MAX_UF);
	m_config = c;
	store_eeprom();
	// Gains shape the clamp response and are safe to update live; the auto-arm
	// state is applied only at boot (or via mm_config_apply) so a config write
	// never silently arms a running bench.
	push_gains();
	return true;
}

static int get_cfg_xml(uint8_t **buffer) {
	*buffer = (uint8_t*)mm_config_xml;
	return sizeof(mm_config_xml);
}

void mm_config_apply(void) {
	push_gains();
	autoarm();
}

void mm_config_init(void) {
	load_eeprom();
	mm_config_apply();
	conf_custom_add_config(get_cfg, set_cfg, get_cfg_xml);
}

const mm_config_t *mm_config_get(void) {
	return &m_config;
}
