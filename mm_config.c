// Molten MOSFET: persistent dyno config as a stock-VESC-Tool custom-config
// page. Two jobs:
//   1. Turn one measurable knob — the DC-link capacitance C_dc — into the
//      bus-clamp voltage-loop gains (Ki = wn²·C, Kp = 2ζ·wn·C at a fixed
//      target wn/ζ), so the clamp response stays invariant across bus banks.
//   2. Optionally auto-arm the bus clamp at boot from a stored configuration
//      (default off: the armed-only, nothing-burns-unless-commanded posture).
//
// The config is exposed to VESC Tool via conf_custom_add_config(); stock
// firmware already routes the COMM_*_CUSTOM_CONFIG* packets to
// conf_custom_process_cmd(), so no comms plumbing is added. The wire format
// (get_cfg/set_cfg below) mirrors the confparser convention and MUST stay in
// lockstep with the embedded XML descriptor and MM_CONFIG_SIGNATURE — both are
// regenerated together by documentation/mm_config_xml_gen.py.

